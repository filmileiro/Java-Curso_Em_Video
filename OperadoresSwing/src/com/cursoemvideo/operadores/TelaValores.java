    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:

        int n = Integer.parseInt(txtNumerador.getText());
        int d = Integer.parseInt(txtDeniminador.getText());
 
        float div = n / d;
        float raiz =(float)Math.sqrt(n);
        float resto = n % d;
        float quadrado = (float)Math.pow(n,2);

        lblDiv.setText(Float.toString(div));
        lblRaiz.setText(Float.toString(raiz));
        lblResto.setText(Float.toString(resto));
        lblQuadrado.setText(Float.toString(quadrado));
        
        
        

    }//GEN-LAST:event_btnCalcActionPerformed

