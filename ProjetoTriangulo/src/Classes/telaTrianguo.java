    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:

        int a = sliA.getValue();
        int b = sliB.getValue();
        int c = sliC.getValue();

        if (a < b + c && b < a + c && c < a + b) {
            lblTriangulo.setText("Formam Um tri√¢ngulo");
            if (a == b && b == c) {
                lblTipo.setText("Triangulo Equil√°tero");
            } else if (a != b && b != c && a != c) {
                lblTipo.setText("Tri√¢ngulo Escaleno");
            } else {
                lblTipo.setText("Triangulo Is√≥sceles");
            }

        } else {
            lblTriangulo.setText("N√£o formam uma Tri√¢ngulo");
            lblTipo.setText("-----------");
        }

        panResultado.setVisible(true);
    }//GEN-LAST:event_btnVerificarActionPerformed

